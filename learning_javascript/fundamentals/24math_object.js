//Math object give us all the manipulation on numbers

//takes two arguements and returns the power
console.log(Math.pow(2,3)); //8

//square root 
console.log(Math.sqrt(16)); //4

//find the least value
console.log(Math.min(100,-1,5,0)) //-1

//find the max number
console.log(Math.max(999,1000,12546,-1)); 

//round to the nearest value
console.log(Math.round(145.89647)); //146

//round to upward
console.log(Math.ceil(156.4785)) //157

//round to downward
console.log(Math.floor(156.9999)) //156

//generate random number between 0 and 1
console.log(Math.random());

//generate random number between 0 and 100
console.log(Math.ceil(Math.random()*100));