//typeof is an operator that tells about the data type

var name = 'sai kumar';
console.log(typeof name);

var age = 26;
console.log(typeof age);

var isIndian = true;
console.log(typeof isIndian);

var city = null;
console.log(typeof city);

var dob;
console.log(typeof dob);

var isSymbol = Symbol('123');
console.log(typeof isSymbol);

var child = {
    name : 'rohit',
    age: 2
}

console.log(typeof child);

//Null, Object, Array and Functions all these data types are Objects only

